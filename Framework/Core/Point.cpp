#include "Framework.h"
#include "Point.h"

//////////////////////////////////////////////////////////////////////////

const FPoint FPoint::Zero = FPoint(0, 0);

//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief 생성자
//////////////////////////////////////////////////////////////////////////
FPoint::FPoint(void)
{
	X = 0;
	Y = 0;
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief 생성자
///@param x : X
///@param y : Y
//////////////////////////////////////////////////////////////////////////
FPoint::FPoint(int x, int y)
{
	X = x;
	Y = y;
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief float*
///@return 결과 포인터
//////////////////////////////////////////////////////////////////////////
FPoint::operator float* ()
{
	return (float*)P;
}

FPoint::operator const float* ()
{
	return (float*)P;
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief ==
///@param value2 : 포인트
///@return 결과
//////////////////////////////////////////////////////////////////////////
bool FPoint::operator==(const FPoint& value2) const
{
	if (X == value2.X)
		return Y == value2.Y;
	else
		return false;
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief !=
///@param value2 : 포인트
///@return 결과
//////////////////////////////////////////////////////////////////////////
bool FPoint::operator!=(const FPoint& value2) const
{
	if (X == value2.X)
		return Y != value2.Y;
	else
		return true;
}


FVector2D FPoint::ToVector2()
{
	return FVector2D((float)X, (float)Y);
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief 문자열로 변환
///@param 문자열
//////////////////////////////////////////////////////////////////////////
std::string FPoint::ToString()
{
	std::string temp = "";

	temp += std::to_string(X);
	temp += "," + std::to_string(Y);

	return temp;
}